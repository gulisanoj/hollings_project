def fetch_state_data(stations, state_abbr):
    """Loops through stations, downloads raw data, computes mm, and returns list of DataFrames."""
    print(
        f"\n[2/2] Connecting to hourly precip stream for {len(stations)} stations in {state_abbr}..."
    )
    state_dfs = []

    for idx, station in enumerate(stations):
        print(
            f"    -> [{idx+1}/{len(stations)}] Pulling records for {station['id']} ({station['name']})..."
        )

        # Convert coordinates to your required EPSG:5070 spatial reference locally
        x_5070, y_5070 = transformer.transform(station["lon"], station["lat"])

        query_params = {
            "network": f"{state_abbr}_ASOS",
            "station": station["id"],
            "year1": str(START_YEAR),
            "month1": "1",
            "day1": "1",
            "year2": str(CURRENT_YEAR),
            "month2": str(datetime.now().month),
            "day2": str(datetime.now().day),
            "tz": "America/Chicago",
        }

        try:
            response = requests.get(BASE_DATA_URL, params=query_params, timeout=30)

            if response.status_code == 200 and "station" in response.text:
                df = pd.read_csv(io.StringIO(response.text))

                if df.empty or "precip_in" not in df.columns:
                    continue

                # Data processing and numerical conversions
                df["precip_in"] = pd.to_numeric(df["precip_in"], errors="coerce")
                df["valid"] = pd.to_datetime(df["valid"])
                df = df.sort_values("valid").set_index("valid")

                # Compute rolling 3-hour sum and convert to millimeters
                df["3HR_TOTAL_MM"] = (
                    df["precip_in"].rolling("3h", closed="right").sum() * 25.4
                )
                df["3HR_TOTAL_MM"] = df["3HR_TOTAL_MM"].round(2)

                # Filter condition targeting rows >= 6.35 mm
                df_filtered = df[df["3HR_TOTAL_MM"] >= 6.35].copy()

                if df_filtered.empty:
                    continue

                # Map columns to match your template layout
                df_filtered = df_filtered.reset_index()
                df_filtered["STATE"] = state_abbr  # Added column for clarification
                df_filtered["STATION"] = station["id"]
                df_filtered["X_5070"] = round(x_5070, 2)
                df_filtered["Y_5070"] = round(y_5070, 2)
                df_filtered["WINDOW_END_UTC"] = df_filtered["valid"].dt.strftime(
                    "%Y-%m-%d %H:%M"
                )

                final_df = df_filtered[
                    [
                        "STATE",
                        "STATION",
                        "X_5070",
                        "Y_5070",
                        "WINDOW_END_UTC",
                        "3HR_TOTAL_MM",
                    ]
                ]
                state_dfs.append(final_df)

        except Exception as e:
            print(f"        ⚠️ Skipped {station['id']} due to error: {e}")

        # Rate limiter protect connection
        time.sleep(0.5)

    return state_dfs


